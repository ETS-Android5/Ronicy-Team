package com.adeasy.advertise.ui.chat;

public class AboutUs {

    private String name;
    private String email;

    public AboutUs() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
